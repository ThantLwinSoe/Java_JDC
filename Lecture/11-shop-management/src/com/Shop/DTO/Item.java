package com.Shop.DTO;

public class Item {
	
	private int id;
	private String name;
	private int price;
	private int quantity;
	private String remark;
	
	public Item(int id, String name, int price, int quantity, String remark) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
		this.quantity = quantity;
		this.remark = remark;
	}
	
	

}
